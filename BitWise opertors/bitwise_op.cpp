#include<iostream>
using namespace std;

int main()
{
    int a=4,b=8;
    cout<<"Bitwise And : " <<(a & b) <<endl;
    cout<<"Bitwise Or : " <<(a | b) <<endl;
    cout<<"Bitwise Xor : " <<(a ^ b) <<endl;
    cout<<"Bitwise Left Shift : " <<(10<<2) <<endl;
    cout<<"Bitwise Right Shift : " <<(8>>1) <<endl;
    return 0;
}